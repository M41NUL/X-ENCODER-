progress_bar() {
    local label="$1"
    local total=40
    local bar_width=38
    for i in $(seq 1 $total); do
        filled=$(( bar_width * i / total ))
        empty=$(( bar_width - filled ))
        bar=$(printf '%0.sв–Ҳ' $(seq 1 $filled) 2>/dev/null)
        bar+=$(printf '%0.sв–‘' $(seq 1 $empty) 2>/dev/null)
        pct=$(( 100 * i / total ))
        printf "\r  ${G}${B}[%-38s]${RST} ${W}%3d%%  %s${RST}" "$bar" "$pct" "$label"
        sleep 0.04
    done
    echo ""
}

ok_msg()   { echo -e "  ${G}${B}вң”${RST}  ${W}$1${RST}"; }
skip_msg() { echo -e "  ${O}${B}вҠҳ${RST}  ${W}$1  (already installed)${RST}"; }
step_msg() { echo -e "\n  ${O}${B}вһӨ${RST}  ${W}$1${RST}"; }

